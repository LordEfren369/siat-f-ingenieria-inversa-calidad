    private void jBAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBAgregarActionPerformed
        try {
            String usuario = jTFUsuario.getText();
            String contraseña = jPFContraseña.getText();
            String tipoPerfil = jCBTipoPerfil.getSelectedItem().toString();

            Usuario u = new Usuario(usuario, contraseña, tipoPerfil);

            if (new OAD_Usuario().agregarUsuario(u)) {
                JOptionPane.showMessageDialog(null, "El usuario se agregó correctamente", "Datos guardados", JOptionPane.INFORMATION_MESSAGE);
                jTFUsuario.setText("");
                jPFContraseña.setText("");
                llenarTablaUsuarios();
            } else {
                JOptionPane.showMessageDialog(null, "El usuario no se pudo agregar", "Error al guardar", JOptionPane.ERROR_MESSAGE);
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Error", "Error", HEIGHT);
        }
    }//GEN-LAST:event_jBAgregarActionPerformed

    private void llenarTablaUsuarios() {
        DefaultTableModel modelo = (DefaultTableModel) jTableUsuarios.getModel();
        modelo.setRowCount(0);

        List<Usuario> lista = new OAD_Usuario().obtenerUsuarios();

        if (lista != null) {
            for (Usuario u : lista) {
                modelo.addRow(new Object[]{
                    u.getUsuario(),
                    u.getContraseña(),
                    u.getTipoPerfil()
                });
            }
        } else {
            System.out.println("No se pudo obtener la lista de usuarios.");
        }
    }

