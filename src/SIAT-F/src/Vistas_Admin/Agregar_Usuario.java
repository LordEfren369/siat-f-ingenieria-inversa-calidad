    private void jBEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBEliminarActionPerformed
        int fila = jTableUsuarios.getSelectedRow();

        if (fila < 0) { //Si no selecciona un usuario
            JOptionPane.showMessageDialog(this, "Seleccione un usuario de la tabla");
            return;
        }

        String usuario = jTableUsuarios.getValueAt(fila, 0).toString();

        //confirmacion de querer eliminarlo
        int confirm = JOptionPane.showConfirmDialog(this, "¿Seguro que deseas eliminar al usuario: " + usuario + "?", "Confirmar eliminación", JOptionPane.YES_NO_OPTION);

        if (confirm == JOptionPane.YES_OPTION) {

            boolean ok = new OAD_Usuario().eliminarUsuario(usuario);

            if (ok) {
                JOptionPane.showMessageDialog(this, "Usuario eliminado");
                llenarTablaUsuarios();
            } else {
                JOptionPane.showMessageDialog(this, "Error al eliminar");
            }
        }
    }//GEN-LAST:event_jBEliminarActionPerformed

